package utils

func DedupSlice[T comparable](s []T) []T {
	seen := make(map[T]struct{}, len(s))
	j := 0
	for _, v := range s {
		if _, ok := seen[v]; ok {
			continue
		}
		seen[v] = struct{}{}
		s[j] = v
		j++
	}
	return s[:j]
}

func Append[T comparable](s []T, vs ...T) []T {
next:
	for _, v := range vs {
		for _, vv := range s {
			if v == vv {
				continue next
			}
		}
		s = append(s, v)
	}
	return s
}
